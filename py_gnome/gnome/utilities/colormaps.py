class NewColorMap(ColorMap):
    def __init__(self, colorFunction):
        self.colorFunction = colorFunction

    def get_color(self, values, ValRange=(0, 255), out_type='int'):
        """
        values -- is a tuple with one entry
        """
        # find fraction between 0 and 1
        val = values[0]
        totalRange = ValRange[1] - ValRange[0]
        if totalRange == 0:
            frac = 0.0
        else:
            frac = val / float(totalRange)

        if frac < 0:
            frac = 0.0
        if frac > 1:
            frac = 1.0

        rgbTuple = self.colorFunction(frac)

        return [rgbTuple]


